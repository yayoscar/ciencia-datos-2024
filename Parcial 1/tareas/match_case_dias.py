dia = input("Ingresa un día de la semana: ")
match dia:
     case "Lunes":
        print("Monday")
     case "Martes":
        print("Tuesday")
     case "Miércoles":
        print("Wednesday")
     case "Jueves":
        print("Thursday")
     case "Viernes":
        print("Friday")
     case "Sábado":
        print("Saturday")
     case "Domingo":
        print("Sunday")