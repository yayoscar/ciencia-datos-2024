# Solicita una frase al usuario
frase = input("Ingrese una frase: ")

# Muestra la frase en minúsculas
print("Minúsculas:", frase.lower())

# Muestra la frase en mayúsculas
print("Mayúsculas:", frase.upper())

# Muestra la frase con la primera letra en mayúscula
print("Capitalizada:", frase.capitalize())