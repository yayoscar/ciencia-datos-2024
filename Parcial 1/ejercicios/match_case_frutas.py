# Match_case_frutas
fruta = input("Ingresa el nombre de una fruta")

match fruta:
    case "Fresa":
        print("Fresa")
    case "uva":
        print("Uva")
    case "Maracuya":
        print("Maracuya")
    case "Platano":
        print("Platano")
    case "Naranja":
        print("Naranja")
    case "Sandía":
        print("Sandía")
    case "":
        print("Esa no es una fruta")

